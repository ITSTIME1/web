// DOM 이란 
// 우선 웹 브라우저라는건 웹 콘텐츠를 표시해주는 역할을 하게 되는데
// 우리가 서버로부터 웹 페이지를 요청했을때 그 요청을 받아주고
// html 코드를 해석해서 사용자에게 보여주는 역할을 한다고 보면된다. 이게 바로 브라우저가 하는 역할이고
// 이 브라우저는 html 코드를 해석해서 요소들을 트리 형태로 구조화해 표현하여 문서를 생성하게 되는데
// 이를 dom 이라고 한다.

// 그럼 사용자로부터 서버에 요청 -> 서버 응답 -> 데이터 전송 -> 웹브라우저가 코드를 받고
// 브라우저가 코드를 해석 -> DOM파일 생성(Tree 형태로 된 데이터 문서를 만들고) -> 브라우저는 DOM을 바탕으로 화면에 웹 콘텐츠를 보여줌.


// dom은 왜 존재하는가?
